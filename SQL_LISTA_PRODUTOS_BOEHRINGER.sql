--SELEÇÃO DE PEDIDOS COM PRODUTOS SEM ESTOQUE EM QA
SELECT DISTINCT SDLITM ,COUNT(*) FROM PRODDTA.F5547011 , PRODDTA.F4211
WHERE SADOCO = SDDOCO  
AND SAOORN = 'BO' 
AND SDLITM  IN ('10010',  '10339', '10688', '11286', '11521', '11559', '11729',
        '12642' ,'12643', '13184', '15727', '15728', '34748', '35433')
 GROUP BY SDLITM
 
 
