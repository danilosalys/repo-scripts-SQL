SELECT DISTINCT DB_EDIP_COMPRADOR CNPJ,
                to_number(DB_EDIP_OBS1)      AS "NUMERO DO PEDIDO",
                FDDOCO,
                FDBNNF||';',
                FDBSER,
                FDN001,
                NCRCD1,
                DB_EDIP_PEDMERC,
                FHEV01
  FROM PRODDTA.F55NFE02 A,
       PRODDTA.F7611B,
       PRODDTA.F55MCN01 V,
       MERCANET_PRD.DB_PEDIDO@DC09,
       MERCANET_PRD.DB_EDI_PEDIDO@DC09,
       PRODDTA.F5547011
 WHERE FDDOCO = SADOCO
   AND TRIM(SAVR01) = TO_CHAR(DB_PED_NRO)
   AND DB_PED_NRO = DB_EDIP_PEDMERC
   AND DB_EDIP_VAN = 511
   AND DB_EDIP_lote in (1419837,1419839)
   AND FDBNNF = NCBNNF
   AND FDBSER = NCBSER
   AND FDN001 = NCN001
   AND FDBNNF = FHBNNF(+)
   AND FDBSER = FHBSER(+)
   AND FDN001 = FHN001(+)
  -- AND FHEV01 = ' '
   --AND NCRCD1 = '100'
   AND FHBSER IS NULL
   
  
