SELECT A.DM03_GRUPO, b.* FROM MERCANET_PRD.MDM03 a, MERCANET_PRD.MDM05 b
WHERE DM03_DESCRICAO LIKE '%PHL%'
AND DM03_GRUPO IN ('IMPORT FTP')
AND DM03_GUID = DM05_GUID 
AND DM05_MSGDATA > TO_DATE ('14/07/2012 00:00:00','dd/mm/yyyy hh24:mi:ss')
order by dm05_msgdata , dm05_guid, dm05_sessaoid, dm05_msgid
