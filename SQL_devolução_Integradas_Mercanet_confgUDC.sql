SELECT *
  FROM proddta.F42119   F42119_DEV,
       proddta.F5542459,
       proddta.F42119   F42119_VEN,
       proddta.F7611B   F7611B_DEV,
       proddta.F5547011,
       proddta.F0005,
       proddta.F42199
 WHERE F42119_DEV.SDDOCO = F5542459.DCDOCO
   AND TRIM(F42119_DEV.SDDCTO) = TRIM(F5542459.DCDCTO)
   AND F42119_DEV.SDODOC = F42119_VEN.SDDOC
   AND TRIM(F42119_DEV.SDODCT) = TRIM(F42119_VEN.SDDCT)
   AND F42119_DEV.SDITM = F42119_VEN.SDITM
   AND F42119_VEN.SDSO08 != '3'
   AND F42119_DEV.SDLTTR NOT BETWEEN '980' AND '989'
   AND F0005.DRSY = '55'
   AND F0005.DRRT = 'JM'
   AND TRIM(F0005.DRKY) = TRIM(F42119_VEN.SDEUSE)
   AND F42119_DEV.SDDOCO = F7611B_DEV.FDDOCO
   AND F42119_DEV.SDDCTO = F7611B_DEV.FDPDCT
   AND F42119_DEV.SDKCOO = F7611B_DEV.FDKCOO
   AND F42119_DEV.SDLNID = F7611B_DEV.FDLNID
   AND F42119_VEN.SDDOCO = F5547011.SADOCO
   AND F5542459.DCDOCO = &DOCO
   AND F42119_DEV.SDOKCO = F42199.SLKCOO
   AND NVL(TRIM(F42119_DEV.SDOORN), 0) = F42199.SLDOCO
   AND F42119_DEV.SDOCTO = F42199.SLDCTO
   AND F42119_DEV.SDOGNO = F42199.SLLNID
   AND F42119_VEN.SDOKCO = F42199.SLRKCO
   AND F42119_VEN.SDDOCO = NVL(TRIM(F42199.SLRORN), 0)
   AND F42119_VEN.SDDCTO = F42199.SLRCTO
   AND F42119_VEN.SDLNID = F42199.SLRLLN
   AND F42119_VEN.SDITM = F42199.SLITM;
